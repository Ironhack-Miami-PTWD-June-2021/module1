// IF RETURN IS IN ONE LINE, YOU DON'T HAVE TO USE CURLY BRACES IN IF-ELSE:

const age = 20;

if (age > 21) console.log("you are allowed to drink 🍻");
else console.log("sorry, here is OJ for you 🍊");

// ternary operator
age >= 21 ? console.log("you are allowed to drink 🍻") : console.log("sorry, here is OJ for you 🍊");
